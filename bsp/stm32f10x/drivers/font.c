static int chinese_font_cmp(const void *m1, const void *m2) 
{
	struct chinese_font *mi1 = (struct chinese_font *) m1;
	struct chinese_font *mi2 = (struct chinese_font *) m2;
	//return mi2->code - mi1->code;
	if(*((u32 *)mi1->code) > *((u32 *)mi2->code))
		return 1;
	else if(*((u32 *)mi1->code) == *((u32 *)mi2->code))
		return 0;
	else
		return -1;
}
